<template>
    <div class="flex items-center justify-center" :class="containerClass">
        <svg
            class="animate-spin"
            :class="sizeClass"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
        >
            <circle
                class="opacity-25"
                cx="12"
                cy="12"
                r="10"
                stroke="currentColor"
                stroke-width="4"
            ></circle>
            <path
                class="opacity-75"
                fill="currentColor"
                d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
            ></path>
        </svg>
        <span v-if="text" class="ms-2 text-sm" :class="textClass">{{ text }}</span>
    </div>
</template>

<script setup>
import { computed } from 'vue'

const props = defineProps({
    size: {
        type: String,
        default: 'md',
        validator: (value) => ['sm', 'md', 'lg', 'xl'].includes(value)
    },
    color: {
        type: String,
        default: 'primary',
        validator: (value) => ['primary', 'white', 'gray', 'black'].includes(value)
    },
    text: {
        type: String,
        default: ''
    },
    fullscreen: {
        type: Boolean,
        default: false
    }
})

const sizeClass = computed(() => {
    const sizes = {
        sm: 'h-4 w-4',
        md: 'h-8 w-8',
        lg: 'h-12 w-12',
        xl: 'h-16 w-16'
    }
    return sizes[props.size]
})

const textClass = computed(() => {
    const colors = {
        primary: 'text-primary-600',
        white: 'text-white',
        gray: 'text-gray-600',
        black: 'text-black'
    }
    return colors[props.color]
})

const containerClass = computed(() => {
    if (props.fullscreen) {
        return 'fixed inset-0 bg-white bg-opacity-75 z-50'
    }
    return ''
})
</script>
